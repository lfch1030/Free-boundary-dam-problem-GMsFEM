function L=localA1D(T,w)

    v(1)=w(T(1));
    v(2)=w(T(2));

aux= quad(@(x)integranB(x),v(1),v(2));

L(1,1)=(1/(v(2)-v(1))).^2*aux;
L(1,2)=-(1/(v(2)-v(1))).^2*aux;
L(2,1)=L(1,2);
L(2,2)=(1/(v(2)-v(1))).^2*aux;

